const pi = 3.1415962;

function doublePi() {
    return pi * 2;
}

function triplePi() {
    return pi * 3;
}

function add(number1, number2) {
    return number1 + number2;
}
function multiply(number1, number2) {
    return number1 * number2;
}

function devide(number1, number2) {
    return number1 / number2;
}



export default pi;
export { doublePi, triplePi, add, multiply, devide };